"use strict";
function greet(name) {
    return "Olá " + name + "!";
}
// Exemplo de uso:
const greeting = greet("Pedro");
console.log(greeting); // Output: Olá Pedro!
function multiply(num1, num2) {
    return num1 * num2;
}
// Exemplo de uso:
const product = multiply(10, 5);
console.log(product); // Output: 50
